


// USE SWITCH STATEMENT TO PRINT THE DAY OF THE WEEK USING A NUMBER VAR "day" WITH VALUE 1 TO 7
//1 - MONDAY  2 - TUESDAY.....

let DAY=1;
switch (DAY) {
    case 1:
        console.log("MONDAY");
        break;
    case 2:
            console.log("TUESDAY");
            break;
    case 3:
        console.log("WEDNESDAY");
        break;
    case 4:
        console.log("THURSDAY");
        break;
    case 5:
            console.log("FRIDAY");
            break;
    case 6:
        console.log("SATURDAY");
        break;
    case 7:
        console.log("SUNDAY");
        break;    
    default:
        console.log("wrong day!");
        
        break;
}
